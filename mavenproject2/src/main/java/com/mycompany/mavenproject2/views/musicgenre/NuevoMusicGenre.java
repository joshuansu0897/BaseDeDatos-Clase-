    private void llenarDatos() {
        nombre.setText(MG.getCode());
        detalles.setText(MG.getDetails());
        nombre.setEditable(false);
    }

    private void guardar() {
        if (!nombre.getText().trim().equals("")) {
            MG.setCode(nombre.getText().trim());
        } else {
            JOptionPane.showMessageDialog(this, "Codigo incompleto");
            return;
        }

        if (!detalles.getText().trim().equals("")) {
            MG.setDetails(detalles.getText().trim());
        }
        try {
            MusicGenreBD.getInstance().saveMusicGenre(MG, lastCode);
            dispose();
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
