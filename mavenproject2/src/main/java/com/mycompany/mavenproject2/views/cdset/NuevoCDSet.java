    private void llenarDatos() {
        nombre.setText(MG.getName());
        numeros.setText(MG.getTotalNumber() + "");
        if (MG.getCompanyCode() != null && !MG.getCompanyCode().trim().equals("")) {
            companyCode.setSelectedItem(getName(MG.getCompanyCode()));
        }
    }

    private void guardar() {
        if (!nombre.getText().trim().equals("")) {
            MG.setName(nombre.getText().trim());
        } else {
            JOptionPane.showMessageDialog(this, "Nombre incompleto");
            return;
        }

        if (!numeros.getText().trim().equals("") && numeros.getText().matches("^[0-9]*$")) {
            MG.setTotalNumber(Integer.valueOf(numeros.getText().trim()));
        } else {
            JOptionPane.showMessageDialog(this, "Error en cantidad");
            return;
        }

        MG.setCompanyCode(getCode(companyCode.getSelectedItem().toString()));

        try {
            CDSetBD.getInstance().saveCDSet(MG);
            dispose();
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    private void llenarCombo() {
        companyCode.removeAll();
        list.forEach(c -> {
            companyCode.addItem(c.getName());
        });
    }

    private String getCode(String name) {
        return list.stream().filter(c -> c.getName().equals(name))
                .findFirst()
                .get()
                .getCode();
    }

    private String getName(String code) {
        return list.stream().filter(c -> c.getCode().equals(code))
                .findFirst()
                .get()
                .getName();
    }

