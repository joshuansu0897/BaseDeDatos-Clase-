    public void llenarTabla() {
        try {
            DefaultTableModel model = (DefaultTableModel) tabla.getModel();
            model.setRowCount(0);
            ArtistBD.getInstance().getMostViews().forEach(c -> {
                model.addRow(c);
            });
            tabla.setModel(model);
        } catch (Exception ex) {
            logger.error("Error al Consultar la base de datos.", ex);
        }
    }

